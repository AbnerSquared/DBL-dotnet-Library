namespace DiscordBotList.Widgets
{
    /// <summary>
    /// Defines the image formats that can be set for a widget.
    /// </summary>
    public enum WidgetImageFormat
    {
        /// <summary>
        /// Marks the widget to build in the SVG (Scalable Vector Graphics) image format.
        /// </summary>
        Svg = 1,

        /// <summary>
        /// Marks the widget to build in the PNG (Portable Network Graphics) image format.
        /// </summary>
        Png = 2
    }
}
